    private void SearchStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchStudentActionPerformed
        String searchElement = getSearchDetails.getText().trim();
        JOptionPane.showMessageDialog(null, "Student Searched By : " + searchElement);
        String query = "SELECT * FROM students WHERE std_reg_no = ? OR std_nic = ?";
        DB_Conn db = new DB_Conn();
        Connection conn = db.getConnection();
        try ( PreparedStatement pstmt = conn.prepareStatement(query)) {
            pstmt.setString(1, searchElement);
            pstmt.setString(2, searchElement);
            ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                std_detials_view.setVisible(true);
                edit_std_details.setVisible(true);
                del_std.setVisible(true);
                std_photo_view.setVisible(true);

                std_name_view.setText(rs.getString("std_name"));
                std_address_view.setText(rs.getString("std_address"));
                std_phone_view.setText(rs.getString("std_phone"));
                std_email_view.setText(rs.getString("std_email"));
                std_nic_view.setText(rs.getString("std_nic"));
                reg_no_view.setText(rs.getString("std_reg_no"));
                view_std_department.setSelectedItem(rs.getString("std_department"));
                std_aca_year_view.setSelectedItem(rs.getString("std_aca_year"));
                std_faculty_view.setSelectedItem(rs.getString("std_faculty"));
                std_believes_view.setSelectedItem(rs.getString("std_believes"));
                parent_name_view.setText(rs.getString("parent_name"));
                parent_address_view.setText(rs.getString("parent_address"));
                parent_phone_view.setText(rs.getString("parent_phone"));
                parent_nic_view.setText(rs.getString("parent_nic"));
                this.dbIdentificationID = rs.getInt("id");

                std_room_view.setText(rs.getString("room_no"));
                this.room_No = rs.getInt("room_no");
                try {
                    Blob photoBlob = rs.getBlob("std_photo");
                    InputStream inputStream = photoBlob.getBinaryStream(); // Convert Blob to InputStream
                    ByteArrayOutputStream byteArrayOutputStream = new ByteArrayOutputStream();
                    byte[] buffer = new byte[1024];
                    int length;
                    while ((length = inputStream.read(buffer)) != -1) {
                        byteArrayOutputStream.write(buffer, 0, length);
                    }
                    byte[] imageBytes = byteArrayOutputStream.toByteArray();
                    ImageIcon originalIcon = new ImageIcon(imageBytes);
                    Image originalImage = originalIcon.getImage();
                    Image resizedImage = originalImage.getScaledInstance(180, 220, Image.SCALE_SMOOTH);
                    ImageIcon resizedIcon = new ImageIcon(resizedImage);
                    std_photo_view.setIcon(resizedIcon);
                } catch (Exception e) {
                    e.printStackTrace(); // Handle the exception
                }
            }
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(HostelManagementSystem.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Student Not Found", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_SearchStudentActionPerformed

